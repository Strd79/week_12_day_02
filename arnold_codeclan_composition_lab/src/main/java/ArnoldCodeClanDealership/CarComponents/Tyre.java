package ArnoldCodeClanDealership.CarComponents;

public class Tyre {

    private String name;
    private int size;

    public Tyre(String name, int size) {
        this.name = name;
        this.size = size;
    }

    public String getName() {
        return name;
    }

    public int getSize() {
        return size;
    }
}
